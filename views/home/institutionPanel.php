            <div class="card card-body">
                Le programme National de l’hygiène aux Frontières est l’un de programme du ministère de la Santé Publique ayant pour charge d’assurer les contrôles sanitaires aux frontières. Il est sous tutelle de la Division de la lutte contre les maladies au secrétariat National de la 4ieme Direction. Il est une structure du secrétariat National de la santé sous tutelle technique de la Division de lutte contre les maladies à potentielles épidémiques de portée internationale. (MAPEPI) et la surveillance des 17 maladies épidémiologiques. <br>
                Le PNHF Sud-Kivu est situé dans la ville de Bukavu, Commune d’Ibanda à Labotte dans l’avenue du lac numéro 2 au Rond-point Riviera non loin de l’assemblée Provinciale du Sud-Kivu et du Bureau de CICR. Il fonctionne dans l’ancien bâtiment du Ministère de la santé Publique du Sud-Kivu. <br>
                Le PNHF est un service spécialisé de la santé Publique qui a pour mission d’assurance de l’hygiène sanitaire aux frontières de la RDC conformément aux RSI de 2005, selon les accords et conventions internationaux et à la législation nationale congolaise. Bien que la mission du PNHF soit exclusivement technique, ses prestations constituent des droits, taxes, redevances à percevoir qui contribuent aux trésors publiques. C’est un service régénérateur des recettes aux trésors publiques travers le Ministère de la Sante Publique. <br>

