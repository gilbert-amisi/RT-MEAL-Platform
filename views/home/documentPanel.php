            <div class="card card-body">
                C'est un document requis pour une circulation libre dans les pays membres de la Communauté économique des pays des Grands Lacs (CEPGL). Ce document a une validité d’une année pour tous les ressortissants des pays membres, et de trois mois pour les expatriées résidant dans l’un des pays membres avec un séjour ne dépassant pas un mois dans un autre pays. Son cout qui sera uniforme dans les trois pays a été fixée à un prix équivalent à 10 dollars américains. Pour l’usage de la carte d’identité comme document de migration pour les populations des zones frontières, la délimitation desdites zones a été limitée aux entités territoriales, urbaines, rurales frontalières juxtaposées. La durée de séjour ne devrait pas excéder trois jours. En plus les utilisateurs de documents CEPGL seront exemptées des visas vers les pays hôtes.

